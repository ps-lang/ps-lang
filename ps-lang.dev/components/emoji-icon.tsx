interface EmojiIconProps {
  emoji: string
}

export function EmojiIcon({ emoji }: EmojiIconProps) {
  return EMOJI_ICON_MAP[emoji] || <span>{emoji}</span>
}

interface EmojiContentProps {
  children: string
}

// Component to process text and replace emojis with icons
export function EmojiContent({ children }: EmojiContentProps) {
  const processText = (text: string) => {
    const parts: (string | JSX.Element)[] = []
    let lastIndex = 0

    // Find all emojis in the text - escape special regex chars and handle variation selectors
    const emojiPattern = Object.keys(EMOJI_ICON_MAP)
      .map(e => e.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'))
      .join('|')
    const emojiRegex = new RegExp(`(${emojiPattern})\\uFE0F?`, 'gu')
    let match

    while ((match = emojiRegex.exec(text)) !== null) {
      // Add text before emoji
      if (match.index > lastIndex) {
        parts.push(text.slice(lastIndex, match.index))
      }

      // Get the base emoji without variation selector
      const baseEmoji = match[1]

      // Add emoji icon with spacing
      parts.push(
        <span key={match.index} className="inline-flex items-start gap-3.5 mr-1">
          <span className="-mt-[3px] flex-shrink-0">
            <EmojiIcon emoji={baseEmoji} />
          </span>
        </span>
      )

      lastIndex = match.index + match[0].length
    }

    // Add remaining text
    if (lastIndex < text.length) {
      parts.push(text.slice(lastIndex))
    }

    return parts.length > 0 ? parts : text
  }

  return <>{processText(children)}</>
}
